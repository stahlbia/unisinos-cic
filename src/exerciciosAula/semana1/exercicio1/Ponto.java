package exerciciosAula.semana1.exercicio1;

public class Ponto {
    // Atributos
    private double x;
    private double y;

    // Construtor
    public Ponto(double x, double y) {
        this.x = x;
        this.y = y;
    }

    // MÃ©todos

    // Get e Set
    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
